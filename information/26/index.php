?><p>
	 Все разнообразие предлагаемых нам для домашнего использования водонагревателей делится на две большие группы - водонагреватели проточные и накопительные. Различие их понятно из названия - в накопительных набранный объем воды греется до определенной температуры, в проточных - подогревается протекающая через нагреватель вода. Чтобы определиться с выбором, рассмотрим их главные достоинства и недостатки.
</p>
<p>
	 Накопительные нагреватели можно встретить повсеместно. Главный плюс накопительных нагревателей - они создают запас солидный горячей воды, обеспечивая ее доступность. Насколько этой воды хватит, зависит от объема бойлера. На стандартные нужды семьи из 3 человек потребуется бойлер объемом желательно не менее сотни литров. Это будет массивный и объемный бак, размещение которого может быть непростой, а то и неразрешимой задачей.
</p>
<p>
	 Основная их техническая характеристика, на которую стоит ориентироваться при выборе - это уровень энергопотребления. Как правило, у бойлеров современных моделей он сравнительно невысок и составляет 1,5-2,5 кВт. Важен тип тэна (водонагревательного элемента). Водонагреватели с сухим (не имеющим контакта с водой) тэном дороже, но они обычно работают дольше, чем водонагреватели с мокрым тэном. «Мокрый» тэн непосредственно контактирует с водой и быстрее выходит из строя (на нем оседают соли, ржавчина и пр.)
</p>
<h2>Выбор модели бойлера </h2>
<p>
