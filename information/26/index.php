</p>
<p>
	 Проточные водонагреватели чуть менее популярны. Они не обеспечат вам запаса нагретой воды, а, чтобы набрать хотя бы половину ванны, придется истратить несколько киловатт электроэнергии (и заплатить за них!) Обратите внимание, что бюджетные модели проточных водонагревателей не имеют достаточной мощности, а потому не смогут обеспечить вам достаточный напор горячей воды. То есть такой сильный, к которому мы привыкли. Тем не менее, проточные нагреватели вполне могут использоваться для того, чтобы принять душ, набрать немного горячей воды для небольшой стирки и т.п.
</p>
<h2>Преимущества проточных нагревателей </h2>
<p>
